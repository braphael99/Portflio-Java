//The purpose of this program is to output Hello World 12 times using a for loop

//creating the public class of HelloWorld
public class HelloWorld{

//starting main
	public static void main(String[] args){
		
		//for loop that iterates 12 times to print "Hello World"
		for(int i = 1; i <= 12; i++)
			{
			System.out.println(i + ". Hello World");
			}
	}
}
